#ifndef __MD5_H__
#define __MD5_H__

#include <stdint.h>
#include <string.h>

#define MD5STRU "%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X"
#define MD5STRL "%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x%02x"
#define MD52STR(hash) hash[0], hash[1], hash[2], hash[3], hash[4], hash[5], hash[6], hash[7], hash[8], hash[9], hash[10], hash[11], hash[12], hash[13], hash[14], hash[15]

typedef struct
{
    uint32_t lo, hi;
    uint32_t a, b, c, d;
    unsigned char buffer[64];
    uint32_t block[16];
} md5_context_t;

extern void md5_init(md5_context_t *ctx);
extern void md5_update(md5_context_t *ctx, const void *data, unsigned long size);
extern void md5_final(unsigned char *result, md5_context_t *ctx);

#endif
